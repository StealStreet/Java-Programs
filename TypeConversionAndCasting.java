
import java.util.Scanner;

public class TypeConversionAndCasting {
    public static void main(String[] args) {
        Scanner sc = new Scanner(System.in);
        float a = 99.98f;
        int b = (int)a;
        char ch = 'a';
        int num = ch;
        System.out.println(num);
        System.out.println(b);
    }
}
